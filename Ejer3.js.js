var numero_uno=prompt("Usuario ingrasa el primer numero entero");
var numero_dos=prompt("Usuario ingresa el segundo numero entero");
var suma=parseInt(numero_uno)+parseInt(numero_dos);
console.log("El resultado de la suma es: "+suma);
var numero_tres=prompt("Usuario ingresa el tercer numero para hacer una multiplicacion");
var multiplicacion =parseInt(suma)*parseInt(numero_tres);
console.log("La multiplicacion del tercer numero y la suma es: "+multiplicacion);
